class PhoneNumber:
    def __init__(self, data):
        if (len(data) == 10):
            self.number = str(data)
        else:
            self.number = str(data).replace(' ','')

    def __str__(self):
        return f"({self.number[0:3]}) {self.number[3:6]}-{self.number[6:10]}"
    def __repr__(self):
        return f"PhoneNumber('{self.number}')"

